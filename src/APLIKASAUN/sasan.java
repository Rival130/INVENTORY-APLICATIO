            String sql = "INSERT INTO sasan VALUES('" + txt_idsasan.getText() + "','" + txt_naransasan.getText() + "','" + txt_folin.getText() + "','" + txt_stok.getText() + "','" + txt_tfolin.getText() + "','" + txt_supplier.getText() + "')";

