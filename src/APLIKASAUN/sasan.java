    public void table() {
        DefaultTableModel tbe = new DefaultTableModel();
        tbe.addColumn("ID SASAN");
        tbe.addColumn("NARAN SASAN");
        tbe.addColumn("FOLIN");
        tbe.addColumn("STOK");
        tbe.addColumn("TOTAL FOLIN");
        tbe.addColumn("NARAN SUPPLIER");

        try {
            Statement st = (Statement) koneksaun.getConnection().createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM sasan");

            while (rs.next()) {
                tbe.addRow(new Object[]{
                    rs.getString("id_sasan"),
                    rs.getString("naran_sasan"),
                    rs.getInt("folin"),
                    rs.getInt("stok"),
                    rs.getInt("total_folin"),
                    rs.getString("naran_supplier")
                });
                table.setModel(tbe);
            }

            //JOptionPane.showMessageDialog(null, "koneksaun sesesu");
        } catch (Exception e) {
            //JOptionPane.showMessageDialog(null, "Koneksaun la Susesu" + e.getMessage());
        }
    }

    

